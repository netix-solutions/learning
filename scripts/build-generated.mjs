// Merge workflow-produced, fact-checked question files into generated-content.mjs.
//
// Reads scripts/generated/<subject>-<grade>.v<k>.json (verified slices written by
// the question-bank workflow), then:
//   • structurally validates each item (4 distinct choices, answer present once),
//   • dedupes within the generated set AND against the curated *-content.mjs,
//   • caps kept items per (subject, grade) so curated+generated ≈ TARGET,
//   • writes scripts/generated-content.mjs (the *_GENERATED exports).
//
// Idempotent: rerun any time after a workflow wave. Run: node scripts/build-generated.mjs

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { SCIENCE_QUESTIONS } from "./science-content.mjs";
import { GEOGRAPHY } from "./geography-content.mjs";
import { HISTORY } from "./history-content.mjs";
import { CIVICS } from "./civics-content.mjs";
import { ECONOMICS } from "./economics-content.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const GEN_DIR = resolve(__dirname, "generated");

const SUBJECTS = {
  science: { curated: SCIENCE_QUESTIONS, export: "SCIENCE_GENERATED", target: 120 },
  geography: { curated: GEOGRAPHY, export: "GEOGRAPHY_GENERATED", target: 80 },
  history: { curated: HISTORY, export: "HISTORY_GENERATED", target: 80 },
  civics: { curated: CIVICS, export: "CIVICS_GENERATED", target: 80 },
  economics: { curated: ECONOMICS, export: "ECONOMICS_GENERATED", target: 80 },
};

const norm = (s) =>
  String(s).toLowerCase().replace(/\s+/g, " ").replace(/[.?!,:;'"]/g, "").trim();

function validItem(it) {
  if (!it || typeof it.prompt !== "string" || !it.prompt.trim()) return false;
  if (typeof it.skill !== "string" || !it.skill) return false;
  if (!Array.isArray(it.choices) || it.choices.length !== 4) return false;
  if (!it.choices.every((c) => typeof c === "string" && c.trim())) return false;
  if (new Set(it.choices.map((c) => c.trim())).size !== 4) return false;
  if (typeof it.answer !== "string" || !it.choices.includes(it.answer)) return false;
  if (typeof it.explanation !== "string" || !it.explanation.trim()) return false;
  return true;
}

const files = readdirSync(GEN_DIR).filter((f) => /\.v\d+\.json$/.test(f));
const exportsOut = {};
const summary = [];

for (const [subject, cfg] of Object.entries(SUBJECTS)) {
  // existing curated prompts per grade (for dedup + cap math)
  const curatedByGrade = {};
  for (const q of cfg.curated) (curatedByGrade[q.grade] ??= []).push(norm(q.prompt));
  const curatedSet = new Set(cfg.curated.map((q) => norm(q.prompt)));

  // collect verified items per grade from <subject>-<grade>.v<k>.json
  const byGrade = {};
  for (const f of files.filter((f) => f.startsWith(`${subject}-`))) {
    const grade = f.slice(subject.length + 1).split(".")[0];
    let arr;
    try {
      arr = JSON.parse(readFileSync(resolve(GEN_DIR, f), "utf8"));
    } catch {
      summary.push(`  ! ${f}: invalid JSON — skipped`);
      continue;
    }
    if (!Array.isArray(arr)) continue;
    (byGrade[grade] ??= []).push(...arr.map((it) => ({ ...it, grade })));
  }

  const kept = [];
  const seen = new Set(curatedSet); // dedupe against curated + across grades
  for (const grade of Object.keys(byGrade).sort()) {
    const existing = (curatedByGrade[grade] ?? []).length;
    const room = Math.max(0, cfg.target - existing);
    let n = 0,
      dropBad = 0,
      dropDup = 0;
    for (const it of byGrade[grade]) {
      if (n >= room) break;
      if (!validItem(it)) {
        dropBad++;
        continue;
      }
      const key = norm(it.prompt);
      if (seen.has(key)) {
        dropDup++;
        continue;
      }
      seen.add(key);
      kept.push({
        grade: it.grade,
        skill: it.skill,
        prompt: it.prompt,
        choices: it.choices,
        answer: it.answer,
        explanation: it.explanation,
      });
      n++;
    }
    summary.push(
      `  ${subject} ${grade}: kept ${n} (existing ${existing} → ~${existing + n}/${cfg.target}; dropped ${dropBad} invalid, ${dropDup} dup)`,
    );
  }
  exportsOut[cfg.export] = kept;
}

// Emit generated-content.mjs
const header = `// AI-authored, independently fact-checked question banks. GENERATED by
// scripts/build-generated.mjs from the question-bank workflow output — do NOT
// hand-edit. The generator concats these onto the curated *-content.mjs arrays.\n\n`;
const body = Object.entries(SUBJECTS)
  .map(([, cfg]) => {
    const items = exportsOut[cfg.export] ?? [];
    const lines = items.map((it) => "  " + JSON.stringify(it) + ",").join("\n");
    return `export const ${cfg.export} = [\n${lines}\n];`;
  })
  .join("\n\n");
writeFileSync(resolve(__dirname, "generated-content.mjs"), header + body + "\n");

console.log("build-generated summary:");
for (const line of summary) console.log(line);
const totals = Object.entries(SUBJECTS).map(
  ([s, cfg]) => `${s} +${(exportsOut[cfg.export] ?? []).length}`,
);
console.log("\nGenerated added:", totals.join(", "));
